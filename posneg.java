import java.util.*;
class posneg{
public static void main(String[]args){
Scanner sc=new Scanner(System.in);
int p=0,n=0,i=0,j=0;
while(n!=-1){
 int nu=sc.nextInt();
if(n==-1){
break;}
else{
if(n>0){
p=p+nu;
i++;}
else{
n=n+nu;
j++;}
}}
System.out.println(p/i);

System.out.println(p/i);
System.out.println(n/j);
}
}